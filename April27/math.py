def add(p1, p2):
    print(f"{p1} + {p2} = {p1 + p2}")


def subtract(p1, p2):
    print(f"{p1} - {p2} = {p1 - p2}")


def divide(p1, p2):
    print(f"{p1} / {p2} = {p1 / p2}")


def multiply(p1, p2):
    print(f"{p1} * {p2} = {p1 * p2}")


